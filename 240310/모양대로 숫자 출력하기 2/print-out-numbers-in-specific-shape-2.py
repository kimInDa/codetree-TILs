# 변수 선언, 입력
n = int(input())

# n * n 크기의 정사각형 출력
for i in range(n):
    for j in range(n):
        # 각 행의 시작 숫자는 2부터 시작해 행이 바뀔 때마다 4씩 증가하며, 8을 초과하면 2로 리셋합니다.
        # 이를 위해 (2 + i*4) % 8 + 2 식을 사용하여 행의 시작 숫자를 결정합니다.
        # 그 후, 각 열에 따라 숫자가 2씩 증가하도록 (j*2)를 더하고, 결과가 8을 초과하지 않도록 전체 합을 8로 나눈 나머지에 2를 더합니다.
        print(((2 + i*4 + j*2) % 8) + 2, end=' ')
    print()  # 각 행이 끝날 때마다 줄바꿈을 해줍니다.