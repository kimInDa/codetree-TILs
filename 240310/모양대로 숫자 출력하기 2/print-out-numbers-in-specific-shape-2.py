# 변수 선언, 입력
n = int(input())

# n * n 크기의 정사각형 출력
for i in range(n):
    for j in range(n):
        # 숫자는 2부터 시작하여 10 미만의 짝수로 순환하므로, (2 + (i+j)) % 8을 사용합니다.
        # 이 때, 2 + (i+j)*2 % 8은 각 숫자가 2씩 증가하되, 8을 넘어가면 다시 2로 돌아가게 만듭니다.
        # 여기서 2를 더하는 이유는 10 미만의 짝수 중 가장 작은 수가 2이기 때문입니다.
        # 마지막으로, % 8 연산 결과에 2를 더해 순환하는 짝수를 만듭니다.
        print(((i + j) * 2) % 8 + 2, end=' ')
    print()  # 각 행이 끝날 때마다 줄바꿈을 해줍니다.