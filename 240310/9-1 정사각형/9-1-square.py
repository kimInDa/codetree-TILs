# 변수 선언, 입력
n = int(input())

# 9부터 1까지 1씩 감소하는 숫자로 이루어진 n * n 정사각형 출력
for i in range(n):
    for j in range(9, 0, -1):
        # i* n + j는 현재 순서를 나타냅니다.
        # 9에서 시작해 1씩 감소하므로, 9 - (i*n + j) % 9를 계산합니다.
        # 단, % 연산의 결과가 0이 되는 경우를 대비해 결과에 1을 더하고, 이를 9에서 빼줍니다.
        # 이렇게 하면 9부터 1까지 순서대로 출력되고, 1 다음에는 다시 9로 돌아가는 패턴이 완성됩니다.
        print(9 - (i*n + j) % 9, end='')
    print()  # 각 행이 끝날 때마다 줄바꿈을 해줍니다.